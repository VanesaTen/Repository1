# earning points using if-elif-else
age = 13

if age < 2:
    print("It's a baby!")
elif age < 4:
    print("Its a toddler!")
elif age < 13:
    print("Its a kid!")
elif age <= 20:
    print("Its a teen!")
elif age < 65:
    print("This is an adult")
else:
    print("Its a elder!")

